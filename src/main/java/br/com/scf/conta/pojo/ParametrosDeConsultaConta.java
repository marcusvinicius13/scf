package br.com.scf.conta.pojo;

public class ParametrosDeConsultaConta {
	
	private String dsConta;
	private String dsStatus;
	private String dsTitular;
	private String valorConta;
	private String dtVencimentoConta;
	
	
	public String getDsConta() {
		return dsConta;
	}
	public void setDsConta(String dsConta) {
		this.dsConta = dsConta;
	}
	public String getDsStatus() {
		return dsStatus;
	}
	public void setDsStatus(String dsStatus) {
		this.dsStatus = dsStatus;
	}
	public String getDsTitular() {
		return dsTitular;
	}
	public void setDsTitular(String dsTitular) {
		this.dsTitular = dsTitular;
	}
	public String getValorConta() {
		return valorConta;
	}
	public void setValorConta(String valorConta) {
		this.valorConta = valorConta;
	}
	public String getDtVencimentoConta() {
		return dtVencimentoConta;
	}
	public void setDtVencimentoConta(String dtVencimentoConta) {
		this.dtVencimentoConta = dtVencimentoConta;
	}
	
}
